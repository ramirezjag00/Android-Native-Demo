public class WhileDoWhileLoops {
  public static void main(String[] args) { 
    // WHILE LOOP
    int i = 0;
    while(i < 5) {
      System.out.println("i = "+i);
      i++;
    }

    // DO WHILE LOOP
    int j = 15;
    do {
      System.out.println("j = "+j);
      j--;
    } while(j > 10);
  }
}
